#include<iostream>
using namespace std;
int main(){
    int a=5,b=2;
    cout<<"a+b is "<<a+b<<endl;
    cout<<"a-b is "<<a-b<<endl;
    cout<<"a*b is "<<a*b<<endl;
    cout<<"a/b is "<<a/b<<endl;
    cout<<"a/b is "<<(float)a/b<<endl; //typecasting :changing data type(int) of a variable into other(float)
    return 0;
}